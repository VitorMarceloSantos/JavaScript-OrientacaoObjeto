/* Orientação a Objeto

 - Um paradigma de programação baseado em objetos, onde eles se interagem entre si.
  - O objeto é uma instância de uma classe, auxilia no reaproveitamente de código.
  - Design Patterns.
  - O objeto é dividido em: propriedades(caracteristicas) e métodos(funcionalidades - funções).
  - JavaScript não é uma linguagem baseada em classes.
  - Possui Prototypes, isso faz com que todos os objetos tenham um pai.
  

*/