// O objeto é a representação de uma classe. Exemplo: class Carros -> objetos: ford, nissan, chevrolet, fiat

/*Classes:
 - É como se fosse o molde do objeto.
 - criam diversos objetos da mesma classe, já possui as propriedades e métodos que os objetos vão utilizar.
 */

 /*Encapsulação: as propriedades e os metodos podem ser utilizados somente por ele, não sendo possível acessar de outro objeto.
  - Quando um objeto contém, ou encapsula, dados ou meios de fazer algo com os dados(usando metodos.)
  - Um outro aspecto da encapsulação é ter propriedades e metódos: públicos, privados ou protegidos.
  - No JS não temos estes meios de forma nativa, tudo é público, porém há como contornar isso.
  */

  /*Agregação:
   - Também conhecido como Composição(composition)
   - o ato de combinar diversos objetos menores em um objeto maior. 
   - isso serve para quando termos um objeto muito grande e complexo.
   - Exemplo: Objeto Carro(maior) contém os objetos menores: objeto Motor(menor), objeto Bancos(menor), objeto Pneus(menor), objeto ParteEletrica(menor).
   */

   /*Herança(inheritance):
    - Quando um objeto ou classe deriva de uma outra classe, podendo herdar suas propriedades e metódos.
    */

    /*Polimorfismo(polymorphism):
     - É a possiblidade de utilizar um método de uma classe pai de maneira diferente, que se adpte as necessidades do novo objeto, sem precisar alterar o método do objeto pai.
     - O polimorfismo utiliza o conceito de herança.*/